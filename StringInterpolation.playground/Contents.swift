import UIKit

/*
String Interpolation: String interpolation allow us to inject values into a String
*/

var age = 31
var ageString = "My age is \(age) years old."
print(ageString)

let birthday = 1993
let currentYear = 2025
let age2 = currentYear - birthday
print("I am \(age2) years old.")

var x = 10
var y = 20
var sum = x + y
print("The sum of \(x) and \(y) is \(sum)")

